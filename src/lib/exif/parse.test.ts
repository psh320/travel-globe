import { describe, expect, it } from "vitest";

import { extractExifMetadata } from "@/lib/exif/parse";

describe("extractExifMetadata", () => {
  it("returns null fields for non-jpeg buffers", () => {
    expect(extractExifMetadata(new Uint8Array([1, 2, 3, 4]).buffer)).toEqual({
      capturedAt: null,
      latitude: null,
      longitude: null,
    });
  });

  it("extracts a captured time from jpeg exif when available", () => {
    const bytes = new Uint8Array([
      0xff, 0xd8, 0xff, 0xe1, 0x00, 0x48,
      0x45, 0x78, 0x69, 0x66, 0x00, 0x00,
      0x49, 0x49, 0x2a, 0x00, 0x08, 0x00, 0x00, 0x00,
      0x01, 0x00,
      0x69, 0x87, 0x04, 0x00, 0x01, 0x00, 0x00, 0x00, 0x1a, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x01, 0x00,
      0x03, 0x90, 0x02, 0x00, 0x14, 0x00, 0x00, 0x00, 0x2c, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x32, 0x30, 0x32, 0x35, 0x3a, 0x30, 0x34, 0x3a, 0x31, 0x30, 0x20, 0x31,
      0x34, 0x3a, 0x33, 0x30, 0x3a, 0x30, 0x30, 0x00, 0xff, 0xd9,
    ]);

    expect(extractExifMetadata(bytes.buffer)).toEqual({
      capturedAt: "2025-04-10T14:30:00.000Z",
      latitude: null,
      longitude: null,
    });
  });
});
